package java_prog;

public class IF_else {

	public static void main(String[] args) {
		
		//print if odd number else even numbers
		
		int i;
		
		for (i=1 ; i<=10 ; i++)
		
		{
			if(i%2==0)	
			{
		
		System.out.println("Print even numbers ==> " +i);
			}
			else{ 
			System.out.println("print odd numbers==>" +i);
			}
		
		
	

		}
		}

}
